function interleave(...groups: Project[][]): Project[] {
  const result: Project[] = [];
  const queues = groups.map((g) => [...g]);
  let i = 0;
  while (queues.some((q) => q.length > 0)) {
    const q = queues[i % queues.length];
    if (q.length > 0) result.push(q.shift()!);
    i++;
  }
  return result;
}

